public class Position {
    private int x;
    private int y;
    
    public Position () {}
    
    public Position (int x, int y) {
        this.x = x;
        this.y = y;
    }
    
    
    
    public void setX(int value)
    {
        x = value;
    }
    public int getX() {
        return x;
    }
    
    public void setY(int value) {
        y = value;
    }
    public int getY() {
        return y;
    }    
    
    

    
}